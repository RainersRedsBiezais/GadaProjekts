              <li>Equipment Required - Dumbbell</li>
              <li>Mechanics - Isolation</li>
              <li>Force Type - Push (Bilateral)</li>
              <li>Experience Level - Beginner</li>    
              <li>Secondary Muscles - None</li>
          </ul>
            <p><b>Seated Dumbbell Tricep Instructions</b></p>
                <ol>
                    <li>Select the desired weight from the rack and position an adjustable bench at 90 degrees.</li>
                    <li>To get into position, sit in an upright position and lift the dumbbell to the top of your shoulder. Take a deep breath, overlap your hands around the dumbbell, then press it into position overhead.</li>
                    <li>Maintain an overlapping grip and slowly lower the dumbbell behind your head by unlocking your elbows.</li>
                    <li>Once your forearms reach parallel or just below, drive the dumbbell back to the starting point by extending the elbows and flexing the triceps.</li>
                    <li>Repeat for the desired number of repetitions.</li>
                </ol>
