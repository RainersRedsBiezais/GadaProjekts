      <div class="modal-body">
      <h5>Exercise Profile</h5>
          <ul>
              <li>Target Muscle Group - Triceps</li>
              <li>Excercise Type - Strength</li>
              <li>Equipment Required - Dumbbell</li>
              <li>Mechanics - Compound</li>
              <li>Force Type - Push (Bilateral)</li>
              <li>Experience Level - Beginner</li>    
              <li>Secondary Muscles - Chest, Shoulders</li>
          </ul>
            <p><b>Dumbbell Floor Press Instructions</b></p>
                <ol>
                    <li>Begin sitting on the floor in an upright position with your legs straight and the dumbbells vertically balanced on the floor.</li>
                    <li>Pick up each dumbbell and set it high in your hip crease while maintaining a tight grip.</li>
                    <li>Slowly lay back while keeping the dumbbells close to your chest and bend your knees to roughly 45 degrees and move your feet up slightly.</li>
                    <li>Press the weights to full extension by contracting your triceps and chest.</li>
                    <li>Slowly lower the weight until both elbows touch the floor then press both dumbbells back to the starting position.</li>
                    <li>Repeat for the desired number of repetitions.</li>
                    <li>To complete the exercise, simply lower the weights to the ground in “controlled drop” fashion. It should be a smooth motion but not one requiring excessive effort or one that puts the shoulder under unnecessary risk.</li>
                </ol>
