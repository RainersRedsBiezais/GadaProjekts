              <li>Target Muscle Group - Triceps</li>
              <li>Excercise Type - Strength</li>
              <li>Equipment Required - Cable</li>
              <li>Mechanics - Isolation</li>
              <li>Force Type - Push (Bilateral)</li>
              <li>Experience Level - Beginner</li>    
              <li>Secondary Muscles - None</li>
          </ul>
            <p><b>Straight Bar Tricep Extension Instructions</b></p>
                <ol>
                    <li>Attach a straight bar to a cable stack as high as possible and assume a standing position.</li>
                    <li>Grasp the straight bar with a pronated grip (palms facing down) and lean forward slightly by hinging at the hips.</li>
                    <li>Initiate the movement by extending the elbows and flexing the triceps.</li>
                    <li>Pull the handle downward until the elbows are almost locked out and then slowly lower under control back to the starting position.</li>
                    <li>Repeat for the desired number of repetitions.</li>
                </ol>
