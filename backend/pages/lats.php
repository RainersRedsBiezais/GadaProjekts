            <p><b>Straight Arm Lat Pull Down Instructions</b></p>
                <ol>
                    <li>Attach a wide grip handle to a cable stack and assume a standing position.</li>
                    <li>Grasp the handle with a pronated grip (double overhand) at roughly shoulder width and lean forward slightly by hinging at the hips.</li>
                    <li>Keep the elbow slightly flexed and initiate the movement by depressing the shoulder blades and extending the shoulders.</li>
                    <li>Pull the bar to your thigh until the lats are fully contracted and then slowly lower under control.</li>
                    <li>Repeat for the desired number of repetitions.</li>
