            <p><b>Concept 2 Rowing Machine Instructions</b></p>
                <ol>
                    <li>Assume a seated position with your feet strapped into the footplates.</li>
                    <li>Take a pronated (double overhand) grip on the bar and sit up upright with the legs and arms extended.</li>
                    <li>Initiate the movement by flexing at the knees and hips while keeping the arms straight.</li>
                    <li>Push your feet into the platform to straighten your legs, lean back slightly, and then proceed to row the handle towards your chest as you exhale to complete the movement.</li>
                    <li>Return to the starting position by lengthening the arms and flexing at the knees and hips.</li>
                    <li>Repeat for the desired time, duration, or distance.</li>
