        </button>
      </div>
      <div class="modal-body">
      <h5>Exercise Profile</h5>
          <ul>
              <li>Target Muscle Group - Shoulders</li>
              <li>Excercise Type - Strength</li>
              <li>Equipment Required - Dumbbell</li>
              <li>Mechanics - Compound</li>
              <li>Force Type - Push (Bilateral)</li>
              <li>Experience Level - Beginner</li>    
              <li>Secondary Muscles - Traps, Triceps</li>
          </ul>
            <p><b>Seated Dumbbell Press Instructions</b></p>
                <ol>
                    <li>Set up an adjustable angle bench to 90 degrees and select the desired weight from the rack.</li>
                    <li>Pick up the dumbbells from the floor using a neutral grip (palms facing in). Position the end of the dumbbells on your knees and sit down on the bench.</li>
                    <li>Using a safe and controlled motion, kick your knees up one at a time in order to get each dumbbell into place.</li>
                    <li>Once the dumbbells are in place, rotate your palms so they are facing forward.</li>
                    <li>Take a deep breath then press the dumbbells overhead by extending the elbows and contracting the deltoids.</li>
                    <li>Slowly lower the dumbbells back to the starting position (the arms should be roughly 90 degrees or slightly lower depending upon limb lengths).</li>
                    <li>Repeat for the desired number of repetitions.</li>
                </ol>
            <p>Video link: <a href="https://youtu.be/FRxZ6wr5bpA" target="_blank">Seated Dumbbell Press Video Exercise Guide</a></p>
