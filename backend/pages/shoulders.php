      </div>
      <div class="modal-body">
      <h5>Exercise Profile</h5>
          <ul>
              <li>Target Muscle Group - Shoulders</li>
              <li>Excercise Type - Strength</li>
              <li>Equipment Required - Barbell</li>
              <li>Mechanics - Compound</li>
              <li>Force Type - Push (Bilateral)</li>
              <li>Experience Level - Intermediate</li>    
              <li>Secondary Muscles - Abs, Traps, Triceps</li>
          </ul>
            <p><b>Overhead Press Instructions</b></p>
                <ol>
                    <li>Adjust the barbell to just below shoulder height then load the desired weight onto the bar.</li>
                    <li>Assume a shoulder width stance and place your hands at (or just outside of) shoulder width with a pronated grip on the bar.</li>
                    <li>Step underneath the bar and unrack it while keeping the spine in a neutral position.</li>
                    <li>Take two steps back, inhale, brace, tuck the chin, then press the bar to lockout overhead.</li>
                    <li>Exhale once the bar gets to lockout and reverse the movement slowly while controlling the bar back to your chest.</li>
                    <li>Repeat for the desired number of repetitions.</li>
                </ol>
            <p>Video link: <a href="https://youtu.be/j7ULT6dznNc" target="_blank">Overhead Press Video Exercise Guide</a></p>    
            
