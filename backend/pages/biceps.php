              <li>Force Type - Pull (Unilateral)</li>
              <li>Experience Level - Beginner</li>    
              <li>Secondary Muscles - Forearms</li>
          </ul>
            <p><b>Concentration Curl Instructions</b></p>
                <ol>
                    <li>Select the desired weight from the rack, and sit in an upright position on a flat bench.</li>
                    <li>Hinge forward and position your elbow near the base of your knee. Place your free hand on the other knee to stabilize yourself.</li>
                    <li>Using a supinated (palms facing up) grip, take a deep breath and curl the dumbbell towards your shoulder.</li>
                    <li>Once the bicep is fully shortened, slowly lower the weight back to the starting position.</li>
                    <li>Repeat for the desired number of repetitions on both sides.</li>
                </ol>
