        </button>
      </div>
      <div class="modal-body">
      <h5>Exercise Profile</h5>
          <ul>
              <li>Target Muscle Group - Chest</li>
              <li>Excercise Type - Strength</li>
              <li>Equipment Required - Bodyweight</li>
              <li>Mechanics - Compound</li>
              <li>Force Type - Push (Bilateral)</li>
              <li>Experience Level - Advanced</li>    
              <li>Secondary Muscles - Abs, Shoulders, Triceps</li>
          </ul>
            <p><b>Plyometric Push Up Instructions</b></p>
                <ol>
                    <li>Assume a quadruped position on the floor with your hands under your shoulders, toes tucked, and knees under hips.</li>
                    <li>Extend one leg at a time and assume a pushup position with the legs straight, elbows extended, and head in a neutral position looking at the floor.</li>
                    <li>Slowly descend to the floor by retracting the shoulder blades and unlocking the elbows.</li>
                    <li>Descend until the upper arms are parallel or your chest touches the floor.</li>
                    <li>Push back forcefully to the starting point by dynamically extending the elbows and driving your palms into the floor. Your goal is to make your hands lose contact with the floor.</li>
                    <li>Repeat for the desired number of repetitions.</li>
                </ol>
            <p>Video link: <a href="https://youtu.be/hxDyN2Bu0uE" target="_blank">Plyometric Push Up Video Exercise Guide</a></p>    
            
