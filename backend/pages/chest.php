          <ul>
              <li>Target Muscle Group - Chest</li>
              <li>Excercise Type - Strength</li>
              <li>Equipment Required - Dumbbell</li>
              <li>Mechanics - Compound</li>
              <li>Force Type - Push (Bilateral)</li>
              <li>Experience Level - Intermediate</li>    
              <li>Secondary Muscles - Chest, Triceps</li>
          </ul>
            <p><b>Dumbbell Twist Press Instructions</b></p>
                <ol>
                    <li>Pick up the dumbbells off the floor using a neutral grip (palms facing in). Position the ends of the dumbbells in your hip crease, and sit down on the bench.</li>
                    <li>To get into position, lay back and keep the weights close to your chest. Once you are in position, take a deep breath, then press the dumbbells to lockout at the top.</li>
                    <li>Slowly lower the dumbbells under control as far as comfortably possible (the handles should be about level with your chest at a 45 degree angle).</li>
                    <li>When pressing the dumbbells back to the starting position, rotate the handles throughout the movement so that the bottom of each faces at the top of the repetition.</li>
                    <li>Repeat for the desired number of repetitions.</li>
                </ol>
