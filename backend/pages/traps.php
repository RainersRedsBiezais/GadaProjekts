              <li>Mechanics - Isolation</li>
              <li>Force Type - Push (Bilateral)</li>
              <li>Experience Level - Beginner</li>    
              <li>Secondary Muscles - Shoulders, Upper Back</li>
          </ul>
            <p><b>Dumbbell Shrug Instructions</b></p>
                <ol>
                    <li>Assume a standing position with the dumbbells on both sides of your body.</li>
                    <li>Hinge forward, inhale, and grab the dumbbells with a neutral grip.</li>
                    <li>Stand up tall and ensure your spine remains neutral.</li>
                    <li>Contract the traps to elevate the shoulders. Squeeze hard at the top and slowly lower the dumbbells back to the starting position.</li>
                    <li>Repeat for the desired number of repetitions.</li>
