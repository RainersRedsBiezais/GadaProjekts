        <h5 class="modal-title" id="shouldersModalLabel">Conventional Deadlift</h5>
        <button type="button" class="close" data-dismiss="modal" aria-label="Close">
          <span aria-hidden="true">&times;</span>
        </button>
      </div>
      <div class="modal-body">
          <h5>Exercise Profile</h5>
          <ul>
              <li>Target Muscle Group - Hamstrings</li>
              <li>Excercise Type - Strength</li>
              <li>Equipment Required - Barbell</li>
              <li>Mechanics - Compound</li>
              <li>Force Type - Hinge (Bilateral)</li>
              <li>Experience Level - Intermediate</li>    
              <li>Secondary Muscles - Abs, Adductors, Calves, Forearms, Glutes, Hamstrings, Lats, Lower Back, Middle Back, Quads, Traps, Upper Back</li>
          </ul>
            <p><b>Conventional Deadlift Instructions</b></p>
                <ol>
                    <li>Position the bar over the top of your shoelaces and assume a hip width stance.</li>
                    <li>Push your hips back and hinge forward until your torso is nearly parallel with the floor.</li>
                    <li>Reach down and grasp the bar using a shoulder width, double overhand grip.</li>
                    <li>Inhale and pull up slightly on the bar while allowing your hips to drop in a seesaw fashion. This phenomenon is commonly referred to as “pulling the slack out of the bar”.</li>
                    <li>As you drop the hips and pull up on the bar, set the lats (imagine you are trying to squeeze oranges in your armpits) and ensure your armpits are positioned directly over the bar.</li>
                    <li>Drive through the whole foot and focus on pushing the floor away.</li>
                    <li>Ensure the bar tracks in a straight line as you extend the knees and hips.</li>
                    <li>Once you have locked out the hips, reverse the movement by pushing the hips back and hinging forward.</li>
                    <li>Return the bar to the floor, reset, and repeat for the desired number of repetitions.</li>
                </ol>
            <p>Video link: <a href="https://youtu.be/wjsu6ceEkAQ" target="_blank">Conventional Deadlift Video Exercise Guide</a></p>    
            
