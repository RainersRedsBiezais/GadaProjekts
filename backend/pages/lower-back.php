            <p><b>Rollover Into V-Sits Instructions</b></p>
                <ol>
                    <li>In a seated position, roll backwards while tucking your chin and allow your legs to travel over your head with your arms outstretched to the sides.</li>
                    <li>Once your toes touch the floor (if possible), roll forward and spread your legs into a straddle position.</li>
                    <li>Reach forward as you roll forward and get a dynamic stretch through hamstrings and spinal erectors.</li>
                    <li>Repeat for the desired number of repetitions.</li>
                </ol>
