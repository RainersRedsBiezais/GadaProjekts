              <li>Target Muscle Group - Glutes</li>
              <li>Excercise Type - Strength</li>
              <li>Equipment Required - Barbell</li>
              <li>Mechanics - Compound</li>
              <li>Force Type - Hinge (Bilateral)</li>
              <li>Experience Level - Intermediate</li>    
              <li>Secondary Muscles - Abs, Hamstrings, Lower Back, Upper Back</li>
          </ul>
            <p><b>Good Mornings Instructions</b></p>
                <ol>
                    <li>Set a bar just below shoulder height in a rack and adjust the safeties to just above hip height.</li>
                    <li>Set your hands equidistant apart, step underneath, and position the bar on your traps (or slightly below if you prefer a low bar version).</li>
                    <li>Begin the movement by unlocking your knees and hinging back into the hips while keeping your spine neutral.</li>
                    <li>Drive through the whole foot as you extend the hip back to the starting position.</li>
                    <li>Repeat for the desired number of repetitions.</li>
                </ol>
