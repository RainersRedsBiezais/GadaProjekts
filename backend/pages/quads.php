              <li>Mechanics - Compound</li>
              <li>Force Type - Push (Bilateral)</li>
              <li>Experience Level - Beginner</li>    
              <li>Secondary Muscles - Abs, Adductors, Calves, Glutes, Hamstrings, Lower Back</li>
          </ul>
            <p><b>Dumbbell Goblet Squat Instructions</b></p>
                <ol>
                    <li>Select a dumbbell and position it at chest height with one hand under each edge of the dumbbell.</li>
                    <li>Take a deep breath and descend by simultaneously pushing the hips back and bending the knees.</li>
                    <li>Once your thighs reach parallel with the floor, begin to reverse the movement.</li>
                    <li>Keep your abs braced and drive your feet through the floor.</li>
                    <li>Drive back to the starting position and repeat for the desired number of repetitions.</li>
                </ol>
