      <div class="modal-body">
          <h5>Exercise Profile</h5>
          <ul>
              <li>Target Muscle Group - Quads</li>
              <li>Excercise Type - Strength</li>
              <li>Equipment Required - Barbell</li>
              <li>Mechanics - Compound</li>
              <li>Force Type - Push</li>
              <li>Experience Level - Intermediate</li>    
              <li>Secondary Muscles - Calves, Glutes, Hamstrings, Lower Back</li>
          </ul>
            <p><b>Barbell Back Squat Instructions</b></p>
                <ol>
                    <li>Set up for the exercise by setting the barbell to just below shoulder height and loading the weight you want to use.</li>
                    <li>Stand under the bar with your feet at about shoulder width apart.</li>
                    <li>Position the bar so that it is resting on the muscles on the top of your back, not on the back of your neck. The bar should feel comfortable. If it doesn't, try adding some padding to the bar.</li>
                    <li>Now take your hands over the back and grip the bar with a wide grip for stability.</li>
                    <li>You should now bend at the knees and straighten your back in preparation to take the weight off the rack.</li>
                    <li>Keeping your back straight and eyes up, push up through the legs and take the weight off the rack.</li>
                    <li>Take a small step back and stabilize yourself.</li>
                    <li>Keeping your eyes facing forward slowly lower your body down. Don't lean forward as you come down. Your buttocks should come out and drop straight down.</li>
                    <li>Squat down until your thighs are parallel with the floor, and then slowly raise your body back up by pushing through your heels.</li>
                    <li>Do not lock the knees out when you stand up, and then repeat the movement.</li>
                </ol>
