            <p><b>Front Squat Instructions</b></p>
                <ol>
                    <li>Position the safety stops at roughly waist height and then place the bar just underneath shoulder height.</li>
                    <li>Position the hands just outside of shoulder width, step underneath the bar, and allow it to sit on the shoulders while driving the elbows high in front of the body.</li>
                    <li>Take a breath and unrack the bar by pushing your hips forward and extending your knees.</li>
                    <li>Take 2-3 steps back and position your feet just inside of shoulder width.</li>
                    <li>Sit directly between your legs by simultaneously bending your knees and pushing your hips back.</li>
                    <li>Once your thighs reach parallel with the floor, begin to reverse the movement.</li>
                    <li>Keep your abs braced and drive your feet through the floor.</li>
                    <li>Finish the lift by exhaling as you fully extend the hips and knees.</li>
